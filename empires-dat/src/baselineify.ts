const baselineify = <T extends any>(baseline: T, original: T) => {
  const item = Object.assign({}, original);

  for (const [key, value] of Object.entries(item)) {
    if (typeof value == "object") {
      item[key] = baselineify(baseline[key], value);
      if (Object.keys(item[key]).length == 0) {
        delete item[key];
      }
    } else if (typeof value == "string" || typeof value == "number") {
      if (baseline[key] == value) {
        delete item[key];
      }
    } else {
      console.log(`property '${key}' type '${typeof value}' not in [object, string, number]`);
    }
  }

  return item;
};

